// igual (==)
// Retorna verdadeiro caso os operados sejam iguais. 3 == var1
"3" == var1
3=== '3'

// Não igual (!=)
// Retorna verdadeiro caso os operandos sejam iguais. var1 != 4
var2 != "3"

// Extritamente igual(===)
// Retorna verdadeiro caso os operandos sejam iguais e do mesmo tipo. Veja também object.is e igualdade
 3 === var1

// Estritamente não igual (!==)
// retorna verdadeiro caso os operandos não sejam iguais e/ou não sejam do mesmo tipo
var1 !== "3"
3 !== '3'

// maior que (>)
// Retorna 
var2 > var1
"12" > 2 

// Maior que ou igual (>=)
// Retorna verdadeiro caso o operando da esqueda seja maior ou igual da direita.
var2 >= var1
var1 >= 3

// Menor que (<)
// Retorna verdadeiro caso o operando da esquerda seja maior que o da direita.
var1 < var2
"12" < "2"

// Menor que ou igual (<=)
// Retorna verdadeiro caso o operando da esquerda seja menor ou igual da direita.
var1 <= var2
2 <= var3